void	ft_swap(int *a, int *b)
{
	int temp;

	temp = *a;
	*a = *b;
	*b = temp;
}

#include <stdio.h>

int main()
{
	int a = 1;
	int b = 3;

	printf("a: %d b:%d\n", a,b);
	ft_swap(&a,&b);
	printf("a: %d b:%d\n", a,b);

}
