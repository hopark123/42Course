#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*p;
	char	*i;

	p = (char *)s;
	i = 0;
	while (*p != '\0')
	{
		if (*p == c)
			i = p;
		p++;
	}
	if (c == 0)
		return (p);
	return (i);
}
